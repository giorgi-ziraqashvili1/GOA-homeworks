# Reversed Strings

def solution(string):
    return string[::-1]


# Convert a String to a Number

def string_to_number(s):
    return int(s)

# Convert a Boolean to a String

def boolean_to_string(b):
    return str(b)

# MakeUpperCase
def make_upper_case(s):
    return s.upper()



# Convert boolean values to strings 'Yes' or 'No'.
def bool_to_word(boolean):
    if boolean == True:
        return "Yes"
    else: 
        return "No"
    
